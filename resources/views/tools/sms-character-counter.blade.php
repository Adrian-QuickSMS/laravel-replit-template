function detectEncoding(text) {
  if (forcedEnc !== 'auto') return forcedEnc;
  for (const ch of text) { if (!isGSM7(ch)) return 'unicode'; }
  return 'gsm';
}

function ucs2Len(text) {
  let n = 0;
  for (const cp of text) { n += cp.codePointAt(0) > 0xFFFF ? 2 : 1; }
  return n;
}

function countEmojis(text) {
  if (typeof Intl !== 'undefined' && Intl.Segmenter) {
    const seg = new Intl.Segmenter('en', { granularity: 'grapheme' });
    let count = 0;
    for (const { segment } of seg.segment(text)) {
      if (/\p{Emoji_Presentation}|\p{Extended_Pictographic}/u.test(segment)) count++;
    }
    return count;
  }
  const re = /\p{Emoji_Presentation}|\p{Extended_Pictographic}/gu;
  return (text.match(re) || []).length;
}

function calcSMS(text, enc) {
  const singleLim = enc === 'gsm' ? 160 : 70;
  const segLim    = enc === 'gsm' ? 153 : 67;
  let effectiveLen = 0;
  if (enc === 'gsm') {
    for (const ch of text) effectiveLen += isGSMExt(ch) ? 2 : 1;
  } else {
    effectiveLen = ucs2Len(text);
  }
  let segs, remaining, fillInSeg, nextSegAt;
  if (effectiveLen === 0) {
    segs = 1; remaining = singleLim; fillInSeg = 0; nextSegAt = singleLim + 1;
  } else if (effectiveLen <= singleLim) {
    segs = 1; remaining = singleLim - effectiveLen; fillInSeg = effectiveLen; nextSegAt = singleLim + 1;
  } else {
    segs = Math.ceil(effectiveLen / segLim);
    const used = effectiveLen % segLim || segLim;
    remaining = segLim - used; fillInSeg = used; nextSegAt = segs * segLim + 1;
  }
  return { effectiveLen, segs, remaining, fillInSeg, singleLim, segLim, nextSegAt };
}

function rcsLengths(text, emojiCount) {
  const codePoints = [...text].length;
  return { basic: codePoints + emojiCount, single: codePoints };
}

