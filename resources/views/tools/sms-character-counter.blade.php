function onDestKey(e) {
  var drop = document.getElementById('destDrop');
  var opts = drop.querySelectorAll('.scc-dest-combo-opt');
  if (e.key === 'ArrowDown') {
    e.preventDefault();
    _hoveredIdx = Math.min(_hoveredIdx + 1, opts.length - 1);
    opts.forEach(function(o, i) { o.classList.toggle('hovered', i === _hoveredIdx); });
    if (opts[_hoveredIdx]) opts[_hoveredIdx].scrollIntoView({ block: 'nearest' });
  } else if (e.key === 'ArrowUp') {
    e.preventDefault();
    _hoveredIdx = Math.max(_hoveredIdx - 1, 0);
    opts.forEach(function(o, i) { o.classList.toggle('hovered', i === _hoveredIdx); });
